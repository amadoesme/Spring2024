public class Escape{
    public static void main(String[] args){
        
        // printing quotation marks
        System.out.print("\"");
        
        //printing a back slash
        System.out.print("\\");
        
        // tab \t
        System.out.print("foo\tbar");
        
        // 
        System.out.print("foo\rbar"); 
    }
}